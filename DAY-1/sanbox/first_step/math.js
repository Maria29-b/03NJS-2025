//fonction  pour l'addition 
function sum (a , b){
return a+b ;
}

//fonction pour soustraction 
function diff (a ,b){
return a-b ; 
}

// fonction pour multiplication
function prod (a, b){
return a*b ;
}

//fonction pour division
function quot (a,b){
return a/b ;
}

module.exports = {sum , diff , prod , quot};
